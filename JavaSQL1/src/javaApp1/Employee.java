package javaApp1;

public class Employee {
	// https://www.tutorialspoint.com/java/java_encapsulation.htm
	 private int id;
	 private String name;
	 private String deptartment;
	 private String role;
	 private int salary;
	 
	 public int getID() {
		 return id;
	 }
	 
	 public String getName() {
		 return name;
	 }
	 
	 public String getDepartment() {
		 return deptartment;
	 }
	 
	 public String getRole() {
		 return role;
	 }
	 
	 public int getSalary() {
		 return salary;
	 }
	 
}
